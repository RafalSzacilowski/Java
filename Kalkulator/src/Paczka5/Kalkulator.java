package Paczka5;

public class Kalkulator {

    public Kalkulator() {
    }

    public double suma(double a , double b){
        double suma = a + b;
        return suma;
    }
    public double roznica(double a , double b){
        double roznica = a - b;
        return  roznica;
    }
    public double iloczyn(double a , double b){
        double iloczyn = a * b;
        return iloczyn;
    }
    public double iloraz(double a , double b){
        double iloraz = a / b;
        return iloraz;
    }
}
